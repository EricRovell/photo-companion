		const map = {
			"zero": [ "часов", "минут" ],
			"one": [ "час", "минута" ],
			"two": [ "часа", "минуты" ],
			"few": [ "часа", "минуты" ],
			"other": [ "часов", "минут" ],
			"many": [ "часов", "минут" ]
		};

		const rules = new Intl.PluralRules("ru-RU");

		return `${arg[0]} ${map[rules.select(arg[0])][0]} ${arg[1]} ${map[rules.select(arg[1])][1]}`;
